package net.transgressoft.lirp.persistence

/**
 * Compile-time resolved descriptor for an [@Indexed][Indexed] property, pairing the index name
 * with a direct property getter function. Instances are generated by the LIRP KSP processor
 * at compile time, eliminating runtime reflection for property value access.
 *
 * @param T The entity type
 * @property indexName The resolved index name (from [Indexed.name] or the property name)
 * @property getter Direct property accessor function — compiled to a regular method call, not reflection
 */
data class IndexEntry<T>(val indexName: String, val getter: (T) -> Any?)